def linkedin_get_member_id(token: str) -> str:
    r = requests.get(
        LINKEDIN_USERINFO,
        headers={"Authorization": f"Bearer {token}"},
        timeout=15,
    )
    if r.status_code == 401:
        raise RuntimeError("LinkedIn token rejected (401). Token expired or invalid.")
    r.raise_for_status()
    data = r.json()
    sub = data.get("sub")
    if not sub:
        raise RuntimeError(f"LinkedIn userinfo response missing 'sub': {data}")
    return sub


def linkedin_publish_post(token: str, member_id: str, text: str) -> str:
    """Publish a text post. Returns the public post URL."""
    body = {
        "author": f"urn:li:person:{member_id}",
        "lifecycleState": "PUBLISHED",
        "specificContent": {
            "com.linkedin.ugc.ShareContent": {
                "shareCommentary": {"text": text},
                "shareMediaCategory": "NONE",
            }
        },
        "visibility": {"com.linkedin.ugc.MemberNetworkVisibility": "PUBLIC"},
    }
    r = requests.post(
        LINKEDIN_UGC_POSTS,
        headers={
            "Authorization": f"Bearer {token}",
            "X-Restli-Protocol-Version": "2.0.0",
            "Content-Type": "application/json",
        },
        data=json.dumps(body),
        timeout=20,
    )
    if r.status_code >= 400:
        raise RuntimeError(f"LinkedIn POST failed [{r.status_code}]: {r.text}")
    urn = r.headers.get("x-restli-id") or r.json().get("id", "")
    # urn looks like urn:li:share:7193... -> URL needs the share id
    share_id = urn.rsplit(":", 1)[-1]
    return f"https://www.linkedin.com/feed/update/urn:li:share:{share_id}/"


