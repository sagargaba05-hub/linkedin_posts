def main() -> int:
    sa_json = env("GOOGLE_SERVICE_ACCOUNT_JSON", required=True)
    sheet_id = env("SHEET_ID", required=True)
    anthropic_key = env("ANTHROPIC_API_KEY", required=True)
    slack_token = env("SLACK_BOT_TOKEN", required=True)
    channel_id = env("SLACK_CHANNEL_ID", required=True)
    linkedin_token = env("LINKEDIN_TOKEN", required=True)
    dry_run = env("DRY_RUN", "0") == "1"

    LOG.info("=== linkedin_automation tick at %s ===", now_local().isoformat())
    if dry_run:
        LOG.info("DRY_RUN enabled — LinkedIn POSTs will be skipped.")

    state = SheetClient.connect(sheet_id, sa_json)
    slack = WebClient(token=slack_token)
    anthropic_client = Anthropic(api_key=anthropic_key)

    bot_user_id = slack_get_bot_user_id(slack)
    member_id = ensure_member_id(state, linkedin_token)

    # Phase 1: process replies on any pending drafts
    try:
        process_pending_drafts(
            state, slack, anthropic_client, channel_id, bot_user_id,
            linkedin_token, member_id, dry_run,
        )
    except Exception:
        LOG.exception("process_pending_drafts failed")

    # Phase 2: generate today's draft if it hasn't happened yet
    try:
        maybe_generate_daily_draft(state, slack, anthropic_client, channel_id)
    except Exception:
        LOG.exception("maybe_generate_daily_draft failed")

    LOG.info("=== tick complete ===")
    return 0


if __name__ == "__main__":
    sys.exit(main())
