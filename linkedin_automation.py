def ensure_member_id(state: SheetClient, token: str) -> str:
    cached = state.state_get("linkedin_member_id")
    if cached:
        return cached
    LOG.info("Fetching LinkedIn member ID for the first time")
    member_id = linkedin_get_member_id(token)
    state.state_set("linkedin_member_id", member_id)
    return member_id


def process_pending_drafts(
    state: SheetClient,
    slack: WebClient,
    anthropic_client: Anthropic,
    channel_id: str,
    bot_user_id: str,
    linkedin_token: str,
    member_id: str,
    dry_run: bool,
):
    """For each draft in 'drafted' state, check Slack thread for user reply."""
    drafts: list[dict] = state.state_get("drafts", [])
    LOG.info("State has %d total drafts", len(drafts))
    for i, d in enumerate(drafts):
        LOG.info("  drafts[%d] sno=%s status=%s thread_ts=%s",
                 i, d.get("sno"), d.get("status"), d.get("thread_ts"))
    if not drafts:
        return

    changed = False
    for d in drafts:
        if d.get("status") != "drafted":
            LOG.info("Skipping SNo. %s (status=%s)", d.get("sno"), d.get("status"))
            continue
        thread_ts = d["thread_ts"]
        LOG.info("Checking SNo. %s (thread_ts=%s) for replies", d.get("sno"), thread_ts)
        reply = slack_get_latest_user_reply(slack, channel_id, thread_ts, bot_user_id)
        if not reply:
            LOG.info("No actionable reply for SNo. %s", d.get("sno"))
            continue
        reply_lc = reply.lower().strip()
        LOG.info("Processing reply for SNo. %s: %r", d.get("sno"), reply_lc[:80])

        if reply_lc.startswith("approve"):
            LOG.info("SNo. %s approved — posting to LinkedIn", d["sno"])
            try:
                if dry_run:
                    post_url = "https://example.com/dry-run-post"
                    LOG.info("[DRY_RUN] Would have posted to LinkedIn")
                else:
                    post_url = linkedin_publish_post(linkedin_token, member_id, d["draft"])
                d["status"] = "posted"
                d["post_url"] = post_url
                d["posted_at"] = now_local().isoformat()
                state.update_row(
                    d["row_number"],
                    {
                        "status": "posted",
                        "post_url": post_url,
                    },
                )
                slack_post_followup(
                    slack, channel_id, thread_ts,
                    f":white_check_mark: Posted to LinkedIn: {post_url}",
                )
                changed = True
            except Exception as e:
                LOG.exception("LinkedIn publish failed for SNo. %s", d["sno"])
                slack_post_followup(
                    slack, channel_id, thread_ts,
                    f":x: LinkedIn post failed: `{e}`. Will retry on next run.",
                )

        elif reply_lc.startswith("reject"):
            LOG.info("SNo. %s rejected", d["sno"])
            d["status"] = "rejected"
            state.update_row(d["row_number"], {"status": "rejected"})
            slack_post_followup(
                slack, channel_id, thread_ts,
                ":no_entry_sign: Marked as rejected. Skipping.",
            )
            changed = True

        elif reply_lc.startswith("regenerate"):
            feedback = reply.split(":", 1)[1].strip() if ":" in reply else ""
            LOG.info("SNo. %s regenerate requested. Feedback: %s", d["sno"], feedback[:80])
            req = DraftRequest(
                topic=d.get("topic", ""),
                angle=d.get("angle", ""),
                key_points=d.get("key_points", ""),
                voice=d.get("voice", ""),
                hook_style=d.get("hook_style", ""),
                link=d.get("link", ""),
                cta=d.get("cta", ""),
                feedback=feedback,
            )
            try:
                new_draft = generate_draft(anthropic_client, req)
                d["draft"] = new_draft
                # Append feedback history to notes
                history = d.get("regen_history", [])
                history.append({"at": now_local().isoformat(), "feedback": feedback})
                d["regen_history"] = history
                slack_repost_in_thread(slack, channel_id, thread_ts, d["sno"], new_draft)
                changed = True
            except Exception as e:
                LOG.exception("Regeneration failed")
                slack_post_followup(
                    slack, channel_id, thread_ts,
                    f":x: Regeneration failed: `{e}`. Try `regenerate: <new feedback>` again.",
                )

        else:
            # Unrecognized reply — do nothing, but only nudge once
            if not d.get("nudged"):
                slack_post_followup(
                    slack, channel_id, thread_ts,
                    "I didn't recognize that. Please reply with `approve`, `reject`, or `regenerate: <feedback>`.",
                )
                d["nudged"] = True
                changed = True

    if changed:
        state.state_set("drafts", drafts)


def maybe_generate_daily_draft(
    state: SheetClient,
    slack: WebClient,
    anthropic_client: Anthropic,
    channel_id: str,
):
    """If today hasn't been drafted yet AND it's past the daily-draft hour, draft now."""
    now = now_local()
    today_str = now.strftime("%Y-%m-%d")
    last_drafted_date = state.state_get("last_drafted_date", "")
    if last_drafted_date == today_str:
        return  # already drafted today

    daily_hour = int(env("DAILY_DRAFT_HOUR", "10"))
    if now.hour < daily_hour:
        return  # too early

    LOG.info("Generating daily draft for %s", today_str)

    pending = state.fetch_pending_rows()
    drafts: list[dict] = state.state_get("drafts", [])
    in_flight_snos = {d["sno"] for d in drafts if d.get("status") in ("drafted", "posted")}

    # Pick first pending row whose SNo is not already in flight
    pick = None
    for row in pending:
        if row["sno"] and row["sno"] not in in_flight_snos:
            pick = row
            break

    if pick:
        LOG.info("Picked SNo. %s — Topic: %s", pick["sno"], pick["topic"][:60])
        req = DraftRequest(
            topic=pick["topic"],
            angle=pick["angle"],
            key_points=pick["key_points"],
            voice=pick["voice"] or "thoughtful",
            hook_style=pick["hook_style"],
            link=pick["link"],
            cta=pick["cta"],
        )
        draft_text = generate_draft(anthropic_client, req)
        thread_ts = slack_post_draft(slack, channel_id, pick["sno"], draft_text)
        drafts.append({
            "sno": pick["sno"],
            "row_number": pick["row_number"],
            "thread_ts": thread_ts,
            "draft": draft_text,
            "topic": pick["topic"],
            "angle": pick["angle"],
            "key_points": pick["key_points"],
            "voice": pick["voice"],
            "hook_style": pick["hook_style"],
            "link": pick["link"],
            "cta": pick["cta"],
            "status": "drafted",
            "drafted_at": now.isoformat(),
            "is_auto": False,
        })
        state.update_row(pick["row_number"], {"status": "drafted"})

    else:
        LOG.info("No pending rows. Falling back to AI-trend mode.")
        theme = pick_trend_theme(state)
        req = DraftRequest(
            topic=theme,
            angle="",
            voice="thoughtful",
        )
        draft_text = generate_draft(anthropic_client, req)
        # Append auto row to the queue so the user has visibility
        auto_sno = f"auto-{today_str}"
        try:
            row_number = state.append_auto_row(
                sno=auto_sno,
                topic=theme,
                voice="thoughtful",
                status="drafted",
            )
        except Exception:
            LOG.exception("Could not append auto row to queue")
            row_number = 0
        thread_ts = slack_post_draft(slack, channel_id, auto_sno, draft_text)
        drafts.append({
            "sno": auto_sno,
            "row_number": row_number,
            "thread_ts": thread_ts,
            "draft": draft_text,
            "topic": theme,
            "angle": "",
            "key_points": "",
            "voice": "thoughtful",
            "hook_style": "",
            "link": "",
            "cta": "",
            "status": "drafted",
            "drafted_at": now.isoformat(),
            "is_auto": True,
        })

    state.state_set("drafts", drafts)
    state.state_set("last_drafted_date", today_str)
    LOG.info("Daily draft posted to Slack.")


