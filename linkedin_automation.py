@dataclass
class SheetClient:
    sheet_id: str
    gc: gspread.Client
    sh: gspread.Spreadsheet
    queue_ws: gspread.Worksheet
    state_ws: gspread.Worksheet
    col_idx: dict[str, int]

    @classmethod
    def connect(cls, sheet_id: str, sa_json: str) -> "SheetClient":
        creds_info = json.loads(sa_json)
        creds = Credentials.from_service_account_info(creds_info, scopes=SHEETS_SCOPES)
        gc = gspread.authorize(creds)
        sh = gc.open_by_key(sheet_id)
        queue_ws = sh.sheet1  # first tab = queue
        # Ensure state tab exists
        try:
            state_ws = sh.worksheet(STATE_TAB_NAME)
        except gspread.WorksheetNotFound:
            LOG.info("Creating %s tab", STATE_TAB_NAME)
            state_ws = sh.add_worksheet(title=STATE_TAB_NAME, rows=200, cols=2)
            state_ws.update("A1:B1", [["key", "value"]])

        headers = queue_ws.row_values(1)
        col_idx = map_columns(headers)
        LOG.info("Mapped queue columns: %s", col_idx)
        return cls(sheet_id, gc, sh, queue_ws, state_ws, col_idx)

    # --- queue tab ---

    def fetch_pending_rows(self) -> list[dict[str, Any]]:
        """Return all rows where Status is empty or 'pending' (case-insensitive)."""
        rows = self.queue_ws.get_all_values()
        if len(rows) < 2:
            return []
        out = []
        status_col = self.col_idx.get("status")
        for i, row in enumerate(rows[1:], start=2):  # row 1 is headers; sheet rows 1-indexed
            row = row + [""] * (max(self.col_idx.values(), default=0) + 1 - len(row))
            status = row[status_col].strip().lower() if status_col is not None else ""
            if status in ("", "pending"):
                out.append({
                    "row_number": i,
                    "raw": row,
                    "sno": self._cell(row, "sno"),
                    "date": self._cell(row, "date"),
                    "topic": self._cell(row, "topic"),
                    "angle": self._cell(row, "angle"),
                    "key_points": self._cell(row, "key_points"),
                    "voice": self._cell(row, "voice"),
                    "hook_style": self._cell(row, "hook_style"),
                    "link": self._cell(row, "link"),
                    "cta": self._cell(row, "cta"),
                    "status": self._cell(row, "status"),
                    "generated_by": self._cell(row, "generated_by"),
                })
        return out

    def _cell(self, row: list[str], key: str) -> str:
        idx = self.col_idx.get(key)
        if idx is None or idx >= len(row):
            return ""
        return str(row[idx]).strip()

    def update_row(self, row_number: int, updates: dict[str, str]):
        """Update specific columns on a given sheet row."""
        for key, value in updates.items():
            idx = self.col_idx.get(key)
            if idx is None:
                LOG.warning("No column mapped for '%s', skipping write", key)
                continue
            col_letter = gspread.utils.rowcol_to_a1(1, idx + 1).rstrip("1")
            cell = f"{col_letter}{row_number}"
            self.queue_ws.update(cell, [[value]])
            LOG.info("Wrote %s=%r to %s", key, value[:60], cell)

    def append_auto_row(self, sno: str, topic: str, voice: str, status: str = "pending"):
        """Append an auto-generated row to the queue. Used for AI-trend fallback."""
        # Build a row in the order of self.queue_ws header columns
        headers = self.queue_ws.row_values(1)
        new_row = [""] * len(headers)
        for key, value in [
            ("sno", sno),
            ("date", now_local().strftime("%Y-%m-%d")),
            ("topic", topic),
            ("voice", voice),
            ("status", status),
            ("generated_by", "Cowork"),
        ]:
            idx = self.col_idx.get(key)
            if idx is not None and idx < len(new_row):
                new_row[idx] = value
        self.queue_ws.append_row(new_row, value_input_option="USER_ENTERED")
        # Find the row number we just appended
        all_rows = self.queue_ws.get_all_values()
        return len(all_rows)

    # --- state tab (key/value) ---

    def state_get(self, key: str, default: Any = None) -> Any:
        rows = self.state_ws.get_all_values()
        for r in rows[1:]:
            if r and r[0] == key:
                if len(r) >= 2 and r[1]:
                    try:
                        return json.loads(r[1])
                    except json.JSONDecodeError:
                        return r[1]
                return default
        return default

    def state_set(self, key: str, value: Any):
        rows = self.state_ws.get_all_values()
        serialized = json.dumps(value) if not isinstance(value, str) else value
        for i, r in enumerate(rows[1:], start=2):
            if r and r[0] == key:
                self.state_ws.update(f"B{i}", [[serialized]])
                return
        self.state_ws.append_row([key, serialized], value_input_option="RAW")


