def slack_post_draft(client: WebClient, channel_id: str, sno: str, draft: str) -> str:
    """Post a new draft to the channel. Returns the thread_ts."""
    text = (
        f":rocket: *Today's LinkedIn Draft (SNo. {sno})*\n\n"
        f"{draft}\n\n"
        f"---\n"
        f"Reply with one of:\n"
        f"• `approve`\n"
        f"• `reject`\n"
        f"• `regenerate: <your feedback>`"
    )
    resp = client.chat_postMessage(channel=channel_id, text=text)
    return resp["ts"]


def slack_repost_in_thread(
    client: WebClient, channel_id: str, thread_ts: str, sno: str, draft: str
) -> None:
    text = (
        f":arrows_counterclockwise: *Regenerated draft (SNo. {sno})*\n\n"
        f"{draft}\n\n"
        f"---\n"
        f"Reply with `approve`, `reject`, or `regenerate: <feedback>`."
    )
    client.chat_postMessage(channel=channel_id, thread_ts=thread_ts, text=text)


def slack_get_latest_user_reply(
    client: WebClient, channel_id: str, thread_ts: str, bot_user_id: str
) -> Optional[str]:
    """Return the text of the latest non-bot reply in the thread, or None."""
    try:
        resp = client.conversations_replies(channel=channel_id, ts=thread_ts, limit=50)
    except SlackApiError as e:
        LOG.warning("Slack thread fetch failed for ts=%s: %s", thread_ts, e.response.get("error"))
        return None
    msgs = resp.get("messages", [])
    LOG.info("Thread %s has %d total messages", thread_ts, len(msgs))
    # Diagnostic: log a compact summary of every message in the thread
    for i, m in enumerate(msgs):
        LOG.info(
            "  msg[%d] user=%s bot_id=%s subtype=%s text_preview=%r",
            i, m.get("user"), m.get("bot_id"), m.get("subtype"),
            (m.get("text") or "")[:60],
        )
    # Reply candidates = anything other than the parent message that wasn't sent
    # by our bot (filter both by user_id and bot_id to be safe)
    user_replies = []
    for m in msgs[1:]:
        if m.get("user") == bot_user_id:
            continue
        if m.get("bot_id"):
            continue
        if not m.get("text"):
            continue
        user_replies.append(m)
    LOG.info("Filtered to %d candidate user replies", len(user_replies))
    if not user_replies:
        return None
    latest_text = user_replies[-1].get("text", "").strip()
    LOG.info("Latest user reply text: %r", latest_text)
    return latest_text


def slack_post_followup(
    client: WebClient, channel_id: str, thread_ts: str, text: str
) -> None:
    client.chat_postMessage(channel=channel_id, thread_ts=thread_ts, text=text)


def slack_get_bot_user_id(client: WebClient) -> str:
    resp = client.auth_test()
    return resp["user_id"]


