"""
LinkedIn Posting Automation
---------------------------
Runs every ~15 min via GitHub Actions. Pulls the next pending row from a Google
Sheet, generates a draft via Anthropic Claude, posts it to a Slack channel for
review, polls the thread for `approve` / `reject` / `regenerate: <feedback>`,
and on approval publishes to LinkedIn and writes status back to the sheet.

State (which SNos are mid-flight, member ID cache, last-drafted-date) lives in
a hidden `_state` tab of the same Google Sheet, as a key/value store.

This script is intentionally idempotent. Running it twice in a minute produces
no duplicate work.

Required env vars (loaded from GitHub Actions secrets):
  GOOGLE_SERVICE_ACCOUNT_JSON  - the entire service account JSON, as a string
  SHEET_ID                     - the Google Sheet ID
  ANTHROPIC_API_KEY            - sk-ant-...
  SLACK_BOT_TOKEN              - xoxb-...
  SLACK_CHANNEL_ID             - the channel ID (e.g. C0B1BRD5PDG), NOT the name
  LINKEDIN_TOKEN               - your LinkedIn access token (60-day rotation)

Optional:
  TZ                           - IANA timezone for daily-trigger logic. Default: Asia/Kolkata
  DAILY_DRAFT_HOUR             - hour (0-23) in TZ to draft at. Default: 10
  ANTHROPIC_MODEL              - default: claude-sonnet-4-6
  DRY_RUN                      - if "1", skip the actual LinkedIn POST (still does everything else)
"""

