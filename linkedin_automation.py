SYSTEM_PROMPT = """You write LinkedIn posts in Sagar's voice — substantive, opinionated, and human. \
You never use AI cliches like "In today's fast-paced world", "Game-changer", "Unlock the power of", \
"Let's dive in", or trailing strings of hashtags. You never use emojis unless explicitly told to. \
You write in plain sentences with line breaks between short paragraphs. Posts are 100-220 words. \
You do not start with "I am excited to" or "Thrilled to share". The opening line is a hook — \
a clear claim, a specific number, a story moment, or a sharp question — never throat-clearing.

If the row provides an Angle, that is the post's central claim. Do not water it down. \
If the row provides Key points, weave all of them in but do not turn the post into a bulleted list. \
If a Voice is specified, match it: thoughtful = considered and slightly contrarian, conversational = \
story-led with a personal moment, punchy = strong opening claim and short paragraphs. \
If a Hook style is specified, open with that pattern.

End the post with the row's CTA if provided; otherwise close with a thought, not a question, unless \
"conversational" voice was requested. If a Link is provided, weave it naturally into a relevant sentence \
near the end - do not just paste it as a footer.

Output only the post text. No preamble, no headers, no markdown."""


@dataclass
class DraftRequest:
    topic: str
    angle: str = ""
    key_points: str = ""
    voice: str = ""
    hook_style: str = ""
    link: str = ""
    cta: str = ""
    feedback: str = ""  # for regenerations


def generate_draft(client: Anthropic, req: DraftRequest) -> str:
    """Call Claude to produce the LinkedIn post text."""
    fields = []
    fields.append(f"Topic: {req.topic}")
    if req.angle:
        fields.append(f"Angle (the central claim): {req.angle}")
    if req.key_points:
        fields.append(f"Key points to weave in:\n{req.key_points}")
    if req.voice:
        fields.append(f"Voice: {req.voice}")
    if req.hook_style:
        fields.append(f"Hook style: {req.hook_style}")
    if req.link:
        fields.append(f"Link to include: {req.link}")
    if req.cta:
        fields.append(f"CTA: {req.cta}")
    if req.feedback:
        fields.append(f"\nUser feedback on the previous draft (address this directly):\n{req.feedback}")

    user_prompt = "Write a LinkedIn post for the following:\n\n" + "\n".join(fields)

    resp = client.messages.create(
        model=ANTHROPIC_MODEL,
        max_tokens=1024,
        system=SYSTEM_PROMPT,
        messages=[{"role": "user", "content": user_prompt}],
    )
    return resp.content[0].text.strip()


def pick_trend_theme(state: SheetClient) -> str:
    """Rotate through TREND_THEMES — pick one we haven't used most recently."""
    last_used = state.state_get("last_trend_theme", "")
    remaining = [t for t in TREND_THEMES if t != last_used]
    pick = remaining[int(time.time()) % len(remaining)]
    state.state_set("last_trend_theme", pick)
    return pick


